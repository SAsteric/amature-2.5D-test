/// <summary>
/// Cardinal facing directions used by both PlayerController and PlayerAnimator.
/// Integer values map directly to sprite-sheet row indices:
///   0 = Down, 1 = Left, 2 = Right, 3 = Up
/// </summary>
public enum Direction
{
    Down  = 0,
    Left  = 1,
    Right = 2,
    Up    = 3
}
